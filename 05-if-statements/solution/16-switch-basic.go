package main

import "fmt"

func main() {
	day := "Tuesday"

	switch day {
	case "Monday":
		fmt.Println("Monday")
	case "Tuesday":
		fmt.Println("Tuesday")
	case "Wednesday":
		fmt.Println("Wednesday")
	case "Thursday":
		fmt.Println("Thursday")
	case "Friday":
		fmt.Println("Friday")
	case "Saturday":
		fmt.Println("Saturday")
	case "Sunday":
		fmt.Println("Sunday")
	}
}
